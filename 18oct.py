# List Methods 
# Inserting --> append, extend, insert(i,obj)
# Removing --> clear, pop(i=-1), remove(i,obj) 
# Functionalities --> reverse, sort, index, count, copy

# Mutable vs Immutable 
# int, float, complex, boolean, None, String are immutable/primitive/non-reference Datatypes (real copy)
# list, tuples, dict, sets are mutable/reference/ non-primitive Datatypes (no real copy only reference)